#ifdef __arm__

#include "ARM6502/M6502mac.h"

	.global memSelector

	.global ramPoke
	.global ramPeek
	.global romPeek
	.global empty_R
	.global empty_W
	.global empty_IO_R
	.global empty_IO_W
	.global rom_W
	.global ram6502W
	.global ram6502R
	.global mem6502W7
	.global mem6502R7


	.syntax unified
	.arm

	.section .text
	.align 2
;@----------------------------------------------------------------------------
empty_R:					;@ Read bad address (error)
;@----------------------------------------------------------------------------
;@----------------------------------------------------------------------------
empty_IO_R:					;@ Read bad IO address (error)
;@----------------------------------------------------------------------------
	mov r11,r11					;@ No$GBA breakpoint
	mov r0,#0x10
	bx lr
;@----------------------------------------------------------------------------
empty_W:					;@ Write bad address (error)
;@----------------------------------------------------------------------------
;@----------------------------------------------------------------------------
empty_IO_W:					;@ Write bad IO address (error)
;@----------------------------------------------------------------------------
	mov r11,r11					;@ No$GBA breakpoint
	mov r0,#0x18
	bx lr
;@----------------------------------------------------------------------------
rom_W:						;@ Write ROM address (error)
	.type rom_W STT_FUNC
;@----------------------------------------------------------------------------
	mov r11,r11					;@ No$GBA breakpoint
	mov r0,#0xB0
	bx lr
;@----------------------------------------------------------------------------
memSelector:
	.byte 0
	.align 2

#ifdef NDS
//	.section .itcm						;@ For the NDS ARM9
#elif GBA
	.section .iwram, "ax", %progbits	;@ For the GBA
#endif
	.align 2

;@----------------------------------------------------------------------------
ramPoke:
	.type ramPoke STT_FUNC
;@----------------------------------------------------------------------------
	ldr r2,=lynxRAM
	strb r1,[r2,r0]
	bx lr
;@----------------------------------------------------------------------------
ramPeek:
	.type ramPeek STT_FUNC
;@----------------------------------------------------------------------------
	ldr r1,=lynxRAM
	ldrb r0,[r1,r0]
	bx lr
;@----------------------------------------------------------------------------
romPeek:
	.type romPeek STT_FUNC
;@----------------------------------------------------------------------------
	ldr r1,=biosSpace
	mov r0,r0,lsl#23
	ldrb r0,[r1,r0,lsr#23]
	bx lr
;@----------------------------------------------------------------------------
ram6502W:					;@ Ram write ($0000-$FFFF)
;@----------------------------------------------------------------------------
	strb r0,[m6502zpage,addy]
	bx lr
;@----------------------------------------------------------------------------
ram6502R:					;@ Ram read ($0000-$FFFF)
;@----------------------------------------------------------------------------
	ldrb r0,[m6502zpage,addy]
	bx lr
;@----------------------------------------------------------------------------
mem6502W7:					;@ Mem read ($E000-$FFFF)
;@----------------------------------------------------------------------------
	ldr r1,[m6502ptr,#m6502MemTbl+28]
	strb r0,[r1,addy]
	bx lr
;@----------------------------------------------------------------------------
mem6502R7:					;@ Mem read ($E000-$FFFF)
;@----------------------------------------------------------------------------
	ldr r1,[m6502ptr,#m6502MemTbl+28]
	ldrb r0,[r1,addy]
	bx lr

;@----------------------------------------------------------------------------
	.end
#endif // #ifdef __arm__
